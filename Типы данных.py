# Типы данных

# integer - int - Целые числа - 3, 79, 0, -35

# Floating Points - float - Числа с точкой - 7.98, -0.905, 1.4

# String - str - Строкове значение -  "Hello!", "Jack", "123"

# List - list - Упорядоченная последовательность объектов - ["Hello", 98, 0.5, "hello"]

# Dictionary - dict - Ключ:Значение -  {"luckyNumber": "777", "name": "Jim"}

# Tuple - tup - Упорядоченная неизменяемая последовательность объектов ("hello", 98, 0.5, 98)

# Set - set - Неупорядоченная коллекция уникальных объектов ("hello", 98, 0.5)

# Boolean - bool - Логические значения - True False

# Пример

my_integer = 1, 2, 3
print(my_integer)

my_float = 1.5, 2.5, 3.5
print(my_float)

my_str = "Hello"
print(my_str)

my_list = ["Hello", "Python"]
print(my_list)

my_dict = {"Name": "John", "City": "New York"}
print(my_dict)

my_tuple = ("Hello", 98, 0.5, 98)
print(my_tuple)

my_set = ("Hello", 98, 0,5) #  Только уникальные значения
print(my_set)

my_bool = True
print(my_bool)

