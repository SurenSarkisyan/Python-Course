# Boolean - Bool - Булевые значения

# Пример

print(1 < 2)
print(type(True))  # bool
print(type(False))  # bool

# Пример операторов сравнения Comparison

print(1 == 1)  # == Оператор равенства (сравнения)
print(1 == 2)
print("Hello" == "Hello")
print("Hello" == "hello")

print(1 != 1)  # != - символ неравенства
print(1 != 2)
print("Hello" != "Hello")
print("Hello" != "hello")

print(1 > 2)
print(1 < 2)
print(1 >= 2)
print(1 <= 2)

print(ord("a"))  # ord - Узнать числовое выражение равное букве из ASCII таблицы
print(ord("b"))
print("a" > "b")

print("hi" > "Hello")
print(ord("i"))  # ord - Узнать числовое выражение равное букве из ASCII таблицы
print(ord("e"))

# Пример присвоения значений переменным и сравнения их между собой

x = 10
